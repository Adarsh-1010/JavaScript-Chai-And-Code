
//While:

let index=1;
while (index<=5) {   //While loop syntax
    console.log(index);
    index++;
}

const arr=["Adarsh","Pathak","Hello","World"];
let i=0;
while (i<arr.length) {
    console.log(arr[i]);
    i++;   
}

//Do While Loop:
let score=1;
do {                       //Do while loop is run once before checking the condition
    console.log(score);
    score++;
} while (score<=5);
